    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int fila = tablaOperacionRealizadas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "no has seleccionado ninguna operacion");
        } else {
            
            miContrFacOperacion.eliminar(id_operacion);
            miContrFacOperacion.limpiarTabla();
            System.out.println(id_operacion);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void tablaOperacionRealizadasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaOperacionRealizadasMouseClicked
        int fila = tablaOperacionRealizadas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "no has seleccionado ninguna operacion");
        } else {
            id_operacion = Integer.parseInt(tablaOperacionRealizadas.getValueAt(fila, 0).toString());
        }
    }//GEN-LAST:event_tablaOperacionRealizadasMouseClicked

