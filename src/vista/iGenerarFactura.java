    void muestramePropietario() {
        List<Propietario> miLista = miControladorPropietario.damePropietarioPorId(id_propietario);
        for (int i = 0; i < miLista.size(); i++) {
            formNombrePropietario.setText(miLista.get(i).getNombre());
            formPrimerApellidoPropietario.setText(miLista.get(i).getPrimer_apellido());
            formPrimerSegundoPropietario.setText(miLista.get(i).getSegundo_apellido());
            formDNIPropietario.setText(miLista.get(i).getDni());
        }
    }

    void muestrameCoche() {
        List<Consulta> miLista = miControladorConsulta.dameConsultaPorId(id_auto);
        for (int i = 0; i < miLista.size(); i++) {
            formModeloConsulta.setText(miLista.get(i).getModelo());
            formMatriculaConsulta.setText(miLista.get(i).getMatricula());
            formAnnoConsulta.setText(String.valueOf(miLista.get(i).getAnno()));
            textAreaDescripcionAveria.setText(miLista.get(i).getDescripcion_averia());
        }
    }

