  public void sumar(){
       int fila;
       int total=0;
       for (int i = 0; i < tablaOperacionRealizadas.getRowCount(); i++) {
           fila=(int) Float.parseFloat(tablaOperacionRealizadas.getValueAt(i, 4).toString());
           total+=fila;
       }
       formTotalPagar.setText(""+total);
   }

